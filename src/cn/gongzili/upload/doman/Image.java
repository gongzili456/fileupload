package cn.gongzili.upload.doman;

public class Image {

	public static final String IMAGE_FORMAT_JPG = "jpg";

	private long id;
	private long uploadDate;
	private String name;
	private String orginalName;
	private String path;
	private String extension;
	private int width;
	private int height;
	private double ratio;
	private long sizeInBytes;
	private String url;
	private ImageExif exif;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public long getUploadDate() {
		return uploadDate;
	}

	public void setUploadDate(long uploadDate) {
		this.uploadDate = uploadDate;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getOrginalName() {
		return orginalName;
	}

	public void setOrginalName(String orginalName) {
		this.orginalName = orginalName;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getExtension() {
		return extension;
	}

	public void setExtension(String extension) {
		this.extension = extension;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public double getRatio() {
		return ratio;
	}

	public void setRatio(double ratio) {
		this.ratio = ratio;
	}

	public ImageExif getExif() {
		return exif;
	}

	public void setExif(ImageExif exif) {
		this.exif = exif;
	}

	public long getSizeInBytes() {
		return sizeInBytes;
	}

	public void setSizeInBytes(long sizeInBytes) {
		this.sizeInBytes = sizeInBytes;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	@Override
	public String toString() {
		return "Image [id=" + id + ", uploadDate=" + uploadDate + ", name="
				+ name + ", orginalName=" + orginalName + ", path=" + path
				+ ", extension=" + extension + ", width=" + width + ", height="
				+ height + ", ratio=" + ratio + "]";
	}

}
